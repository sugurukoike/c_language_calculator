float calc_add(float num1, float num2)
{
    float result;

    result = 0;
    result = num1 + num2;
    return (result);
}

float calc_sub(float num1, float num2)
{
    float result;

    result = 0;
    result = num1 - num2;
    return (result);
}

float calc_mul(float num1, float num2)
{
    float result;

    result = 0;
    result = num1 * num2;
    return (result);
}

float calc_div(float num1, float num2)
{
    float result;

    result = 0;
    result = num1 / num2;
    return (result);
}
